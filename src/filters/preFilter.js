function hasPriorityKeyword(text) {
    const lowerText = text.toLowerCase();
    for (const keyword of PRIORITY_KEYWORDS) {
        if (lowerText.includes(keyword.toLowerCase())) {
            return keyword;
        }
    }
    return null;
}

